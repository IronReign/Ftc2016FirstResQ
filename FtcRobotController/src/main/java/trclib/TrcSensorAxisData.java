package trclib;

public class TrcSensorAxisData
{
    public double x;
    public double y;
    public double z;
    public double timestamp;

    public TrcSensorAxisData(double x, double y, double z, double timestamp)
    {
        this.x = x;
        this.y = y;
        this.z = z;
        this.timestamp = timestamp;
    }   //TrcSensorAxisData

}   //class TrcSensorAxisData
